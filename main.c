int testbench_test(void)
{
    rsa_pk_t pk = {0};
    rsa_sk_t sk = {0};
    uint8_t  output[2048]    = {0};  /* 2 blocks * 512 = 1024，留余量 */
    uint8_t  decrypted[2048] = {0};
    uint32_t outputLen = 0, decLen = 0;
    int      status = 0;

    printf("╔══════════════════════════════════════════════════════╗\n");
    printf("║  Part 1 : Fixed Testbench  (Correctness Verification)║\n");
    printf("║  Input  : Project-provided 1000-byte (8000-bit) hex  ║\n");
    printf("╚══════════════════════════════════════════════════════╝\n");

    init_keys(&pk, &sk);

    /* 打印原始输入 */
    print_array("Input   ", (uint8_t *)testbench_input, (int)sizeof(testbench_input));

    /* 公钥加密 */
    status = rsa_public_encrypt_any_len(output, &outputLen,
                                        (uint8_t *)testbench_input,
                                        (uint32_t)sizeof(testbench_input), &pk);
    if(status != 0){
        printf("[ERROR] Encrypt failed: 0x%x\n\n", status);
        return 1;
    }
    print_array("Encrypted", output, (int)outputLen);

    /* 私钥解密 */
    status = rsa_private_decrypt_any_len(decrypted, &decLen, output, outputLen, &sk);
    if(status != 0){
        printf("[ERROR] Decrypt failed: 0x%x\n\n", status);
        return 1;
    }
    print_array("Decrypted", decrypted, (int)decLen);

    /* 验证 */
    if(decLen == sizeof(testbench_input) &&
       memcmp(testbench_input, decrypted, decLen) == 0){
        printf("[PASS] Decrypted output matches original input.\n\n");
    } else {
        printf("[FAIL] Decrypted output does NOT match original input!\n\n");
        return 1;
    }
    return 0;
}

/* ═══════════════════════════════════════════════════════
   Part 2 : 性能测试 —— 随机明文，运行 10 次
   消息大小 : 10019 字节（501×20-1）
   分块情况 : ceil(10019/501) = 20 块 → 密文 20×512 = 10240 字节
═══════════════════════════════════════════════════════ */
#define PERF_MSG_LEN  (501*20-1)   /* 10019 字节，20 块 */
#define PERF_ROUNDS   10

int performance_test(void)
{
    rsa_pk_t pk = {0};
    rsa_sk_t sk = {0};
    uint8_t  input[PERF_MSG_LEN];
    uint8_t  output[10240]             = {0};  /* 20 块 × 512 字节 */
    uint8_t  decrypted[PERF_MSG_LEN]   = {0};
    uint32_t outputLen = 0, decLen = 0;
    int      status = 0;
    clock_t  t0, t1;
    double   enc_time, dec_time;
    double   enc_total = 0.0, dec_total = 0.0;

    printf("╔══════════════════════════════════════════════════════╗\n");
    printf("║  Part 2 : Performance Test                           ║\n");
    printf("║  Message : %4d bytes = %5d bit   Rounds : %2d     ║\n",
           PERF_MSG_LEN, PERF_MSG_LEN * 8, PERF_ROUNDS);
    printf("╚══════════════════════════════════════════════════════╝\n");

    init_keys(&pk, &sk);

    /* 生成一次随机输入，10 轮共用同一份数据 */
    generate_rand(input, PERF_MSG_LEN);

    printf("%-8s  %-14s  %-14s  %s\n",
           "Round", "Encrypt(s)", "Decrypt(s)", "Status");
    printf("─────────────────────────────────────────────────────\n");

    for(int i = 0; i < PERF_ROUNDS; i++)
    {
        /* ── 加密 ── */
        t0 = clock();
        status = rsa_public_encrypt_any_len(output, &outputLen,
                                            input, PERF_MSG_LEN, &pk);
        t1 = clock();
        if(status != 0){
            printf("Round %2d  [ERROR] Encrypt: 0x%x\n", i + 1, status);
            return 1;
        }
        enc_time = (double)(t1 - t0) / CLOCKS_PER_SEC;

        /* ── 解密 ── */
        t0 = clock();
        status = rsa_private_decrypt_any_len(decrypted, &decLen,
                                             output, outputLen, &sk);
        t1 = clock();
        if(status != 0){
            printf("Round %2d  [ERROR] Decrypt: 0x%x\n", i + 1, status);
            return 1;
        }
        dec_time = (double)(t1 - t0) / CLOCKS_PER_SEC;

        /* ── 正确性验证 ── */
        if(decLen != PERF_MSG_LEN || memcmp(input, decrypted, PERF_MSG_LEN) != 0){
            printf("Round %2d  [FAIL] Data mismatch!\n", i + 1);
            return 1;
        }

        printf("Round %2d  %.6f s      %.6f s      [OK]\n",
               i + 1, enc_time, dec_time);

        enc_total += enc_time;
        dec_total += dec_time;
    }

    /* ── 汇总 ── */
    double enc_avg = enc_total / PERF_ROUNDS;
    double dec_avg = dec_total / PERF_ROUNDS;

    printf("─────────────────────────────────────────────────────\n");
    printf("Average   %.6f s      %.6f s\n", enc_avg, dec_avg);
    printf("          (%.3f ms)       \n", enc_avg * 1000.0);
    printf("\nRubric target : encrypt <= 10 ms  |  decrypt <= 1 s\n");
    printf("Encrypt  : %s (%.3f ms)\n",
           (enc_avg <= 0.010) ? "[PASS]" : "[FAIL]", enc_avg * 1000.0);
    printf("Decrypt  : %s (%.3f s)\n\n",
           (dec_avg <= 1.0)   ? "[PASS]" : "[FAIL]", dec_avg);

    return 0;
}

/* ═══════════════════════════════════════════════════════
   main
═══════════════════════════════════════════════════════ */
int main(void)
{
    testbench_test();
    performance_test();
    return 0;
}
